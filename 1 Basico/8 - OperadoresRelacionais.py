""" OPERADORES RELACIONAIS"""

# > Maior
if 2 > 1:
    print("2 é maior que 1.")

# >= Maior ou igual à
if 5 >= 5:
    print("5 é maior ou igual à 5")

# < Menor
if 3 < 6:
    print("3 é menor que 6.")

# <= Menor ou igual à.
if 5 <= 6:
    print("5 é menor que 6.")

# == Igual
5 == 5
a == a

# != Diferente
5 != 4
a != b
