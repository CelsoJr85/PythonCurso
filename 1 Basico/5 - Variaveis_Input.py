""" VARIAVÉIS E INPUT """

# Variavéis são pequenos pedaços de memorias indexadas.
# Usa se o sinal = para criar uma variavel.
nome = "Celso"
idade = 37


# Variavel pode ser usada para armazenar entradas associado com input
nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: "))

# Variavel pode ser usada em um print. Pegar a variavel nome que recebe input e imprimir ela.
print(f"Seu nome é {nome} e sua idade é {idade}")
